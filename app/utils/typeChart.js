/**
 * Calcula el multiplicador de efectividad total.
 * Se acumula si el defensor tiene dos tipos (e.g. 2x × 2x = 4x).
 */
export function getTypeEffectiveness(moveType, defenderTypes = []) {
  if (!moveType) return 1;
  const chart = TYPE_CHART[moveType] || {};
  let multiplier = 1;
  for (const defType of defenderTypes) {
    multiplier *= chart[defType] ?? 1;
  }
  return multiplier;
}

/**
 * Devuelve el mensaje de efectividad para mostrar en batalla.
 * Retorna null si la efectividad es normal (1x).
 */
export function getEffectivenessMessage(multiplier) {
  if (multiplier === 0)
    return { text: '¡No tiene ningún efecto!', type: 'immune' };
  if (multiplier >= 4)
    return { text: '¡Es extremadamente eficaz!', type: 'supereffective' };
  if (multiplier >= 2)
    return { text: '¡Es súper eficaz!', type: 'supereffective' };
  if (multiplier <= 0.25)
    return { text: 'Apenas tiene efecto...', type: 'notveryeffective' };
  if (multiplier < 1)
    return { text: 'No es muy eficaz...', type: 'notveryeffective' };
  return null;
}
